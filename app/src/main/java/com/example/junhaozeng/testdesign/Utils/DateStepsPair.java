public class DateStepsPair {
    private String date;
    private int steps;
    public DateStepsPair() { }
    public void setDate(String date) {
        this.date = date;
    }
    public void setSteps(int steps) {
        this.steps = steps;
    }
    public String getDate() {
        return date;
    }
    public int getSteps() {
        return steps;
    }
}
